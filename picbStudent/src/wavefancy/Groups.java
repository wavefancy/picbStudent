package wavefancy;

public class Groups {
	int id;
	String pGroupNameChs;
	String pGroupNameEng;
	String pImg;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getpGroupNameChs() {
		return pGroupNameChs;
	}
	public void setpGroupNameChs(String pGroupNameChs) {
		this.pGroupNameChs = pGroupNameChs;
	}
	public String getpGroupNameEng() {
		return pGroupNameEng;
	}
	public void setpGroupNameEng(String pGroupNameEng) {
		this.pGroupNameEng = pGroupNameEng;
	}
	public String getpImg() {
		return pImg;
	}
	public void setpImg(String pImg) {
		this.pImg = pImg;
	}
}
